#include "libft.h"

void	ft_bzero(void *ptr, size_t j)
{
	size_t	i;
	char	*p;

	p = (char *)ptr;
	i = 0;
	while ((i < j) && j > 0)
	{
		p[i] = 0;
		i ++;
	}
}
